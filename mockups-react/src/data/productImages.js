// Helper function to get all products as array
export const getAllProductImages = () => {
  return Object.values(productImages)
}

// Helper function to get products by category
export const getProductsByCategory = (category) => {
  return Object.values(productImages).filter(product => product.category === category)
}

// Helper function to get random products
export const getRandomProducts = (count = 6) => {
  const products = Object.values(productImages)
  const shuffled = products.sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}

// Helper function to search products
export const searchProducts = (query) => {
  const products = Object.values(productImages)
  return products.filter(product => 
    product.name.toLowerCase().includes(query.toLowerCase()) ||
    product.description.toLowerCase().includes(query.toLowerCase()) ||
    product.category.toLowerCase().includes(query.toLowerCase())
  )
}
